export const translateBreadName = (bread, language) => {
  const dictionary = breadNameTranslations[language] || breadNameTranslations.pt;
  return dictionary[bread.id] || bread.name;
};

export const translateInstruction = (instruction, language) => {
  if (language === 'pt') {
    return instruction;
  }

  return instructionTranslations[language]?.[instruction] || instruction;
};

